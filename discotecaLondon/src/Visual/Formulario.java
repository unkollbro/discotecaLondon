    private void btnEntrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEntrarMouseClicked
        if(jListInvitados.isSelectionEmpty()){
            JOptionPane.showMessageDialog(rootPane, "selecciona un invitado");
        }else{
        for (Persona persona : vPersonas) {
            if(jListInvitados.getSelectedValue().equals(persona.getNombre())){
                persona.setPagado(true);
                persona.setAsistido(true);
            }
        }
        establecerAsistentes();
        establecerLista();
        }
    }//GEN-LAST:event_btnEntrarMouseClicked

    private void btnSalirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSalirMouseClicked
        if(jListAsistentes.isSelectionEmpty()){
            JOptionPane.showMessageDialog(rootPane, "selecciona un asistente");
        }else{
            for (Persona persona : vAsistentes) {
                if(jListAsistentes.getSelectedValue().equals(persona.getNombre())){
                    persona.setSalir(true);
                }
            }
            establecerAsistentes();
            establecerLista();
        }
        
    }//GEN-LAST:event_btnSalirMouseClicked
    
