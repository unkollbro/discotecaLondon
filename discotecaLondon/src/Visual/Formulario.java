    private void establecerLista(){
        invitados.clear();
        invitados.addElement(": Invitados :");
        for (Persona persona : vPersonas) {
            if((!persona.isPagado() && !persona.isAsistido()) || persona.isSalir()){
                invitados.addElement(persona.getNombre());
            }
        }
    }
    
    private void establecerAsistentes(){
        asistentes.clear();
        asistentes.addElement(": Asistentes :");
        for (Persona persona : vPersonas) {
            if(persona.isPagado() && persona.isAsistido()){
                asistentes.addElement(persona.getNombre());
            }
        }
    }
